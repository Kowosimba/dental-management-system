    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
          try {
    String IDnum, Name, Surname, Address, Gender, number, Allergies;
    SimpleDateFormat DOB = new SimpleDateFormat("yyyy-MM-dd");

    IDnum = txtid.getText().trim();
    Name = txtname.getText().trim();
    Surname = txtsurname.getText().trim();
    Address = txtaddress.getText().trim();
    Gender = cmbgender.getSelectedItem().toString();
    number = txtnumber.getText().trim();
    Date selectedDate = datechooser1.getDate();
    Allergies = txtallergies.getText().trim();

    // Check for empty fields
    if (IDnum.isEmpty() || Name.isEmpty() || Surname.isEmpty() || Address.isEmpty() || 
        Gender.isEmpty() || number.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields.");
        return; // Exit if any fields are empty
    }

    // Ask for confirmation before updating
    int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to update the patient details?", "Confirm Update", JOptionPane.YES_NO_OPTION);
    if (confirm != JOptionPane.YES_OPTION) {
        return; // Exit if user chooses not to proceed
    }

    Class.forName("com.mysql.jdbc.Driver");
    Connection con = DriverManager.getConnection("jdbc:mysql://localhost/dental", "root", "");
    PreparedStatement ps = con.prepareStatement("UPDATE patients SET Name = ?, Surname = ?, Address = ?, Gender = ?, number = ?, DOB = ?, Allergies = ? WHERE IDnum = ?");

    // Set parameters for the update
    ps.setString(1, Name); // Name
    ps.setString(2, Surname); // Surname
    ps.setString(3, Address); // Address
    ps.setString(4, Gender); // Gender
    ps.setString(5, number); // Number
    ps.setDate(6, new java.sql.Date(selectedDate.getTime())); // DOB
    ps.setString(7, Allergies); // Allergies
    ps.setString(8, IDnum); // IDnum (last)

    // Execute the update
    int rowsUpdated = ps.executeUpdate();
    
    // Check if any rows were updated
    if (rowsUpdated > 0) {
        JOptionPane.showMessageDialog(this, "Record updated successfully");
        update_table();
    } else {
        JOptionPane.showMessageDialog(this, "No changes made. Please check if the details are different.");
    }

} catch (Exception ex) {
    Logger.getLogger(frmpatients.class.getName()).log(Level.SEVERE, null, ex);
    JOptionPane.showMessageDialog(this, "Error updating record: " + ex.getMessage());
}
    }//GEN-LAST:event_jButton8ActionPerformed

    private void PatientTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PatientTableMouseClicked
        DefaultTableModel model = (DefaultTableModel) PatientTable.getModel();
    
    // Setting text fields with selected row data
    txtid.setText(model.getValueAt(PatientTable.getSelectedRow(), 0).toString());
    txtname.setText(model.getValueAt(PatientTable.getSelectedRow(), 1).toString());
    txtsurname.setText(model.getValueAt(PatientTable.getSelectedRow(), 2).toString());
    txtaddress.setText(model.getValueAt(PatientTable.getSelectedRow(), 3).toString());
    cmbgender.setSelectedItem(model.getValueAt(PatientTable.getSelectedRow(), 4).toString());
    txtnumber.setText(model.getValueAt(PatientTable.getSelectedRow(), 5).toString());
    
    // Handling the date
    SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
    String dateString = model.getValueAt(PatientTable.getSelectedRow(), 6).toString();
    
    try {
        Date date = dateFormat.parse(dateString);
        datechooser1.setDate(date); // Assuming datechooser1 is a JDateChooser
    } catch (Exception e) {
        e.printStackTrace(); // Handle parsing errors
        JOptionPane.showMessageDialog(this, "Error parsing date: " + e.getMessage());
    }

    txtallergies.setText(model.getValueAt(PatientTable.getSelectedRow(), 7).toString());
           
            
            
    }//GEN-LAST:event_PatientTableMouseClicked

