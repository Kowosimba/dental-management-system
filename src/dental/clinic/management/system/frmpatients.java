  public void update_table() {
    try {
        String sq = "SELECT * FROM patients ORDER BY IDnum";
        Connection conn = getConnection();
        PreparedStatement ps = conn.prepareStatement(sq);
        ResultSet rs = ps.executeQuery();
        PatientTable.setModel(DbUtils.resultSetToTableModel(rs));
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, ex.getMessage());
    }
}

private Connection getConnection() {
    try {
        Class.forName("com.mysql.jdbc.Driver");
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/dental", "root", "");
        return conn;
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, ex.getMessage());
        return null;
    }
}



    
    
    
