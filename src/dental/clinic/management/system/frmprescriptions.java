    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        if (txtname.getText().equals("") || cmbtreatment.getSelectedItem() == null) {
    JOptionPane.showMessageDialog(this, "Please fill in the name and treatment fields.");
    return; // Exit the method if any required field is empty
}

try {
    String Name = txtname.getText();
    String treatment = cmbtreatment.getSelectedItem().toString();

    Class.forName("com.mysql.jdbc.Driver");
    Connection con = DriverManager.getConnection("jdbc:mysql://localhost/dental", "root", "");

    // Check if the prescription exists before attempting to delete
    PreparedStatement checkStmt = con.prepareStatement("SELECT COUNT(*) FROM prescriptions WHERE name = ? AND treatment = ?");
    checkStmt.setString(1, Name);
    checkStmt.setString(2, treatment);
    ResultSet rs = checkStmt.executeQuery();

    if (rs.next() && rs.getInt(1) == 0) {
        JOptionPane.showMessageDialog(this, "No prescription found with the specified name and treatment.");
        return; // Exit if no prescription exists
    }

    // Prepare SQL statement for deletion
    PreparedStatement ps = con.prepareStatement("DELETE FROM prescriptions WHERE name = ? AND treatment = ?");
    ps.setString(1, Name);
    ps.setString(2, treatment);
    int rowsDeleted = ps.executeUpdate();

    // Check if any rows were deleted
    if (rowsDeleted > 0) {
        JOptionPane.showMessageDialog(this, "Record deleted successfully.");
    } else {
        JOptionPane.showMessageDialog(this, "No changes made. Please check if the details are correct.");
    }

    // Close resources
    rs.close();
    checkStmt.close();
    ps.close();
    con.close();

} catch (Exception ex) {
    Logger.getLogger(frmpatients.class.getName()).log(Level.SEVERE, null, ex);
    JOptionPane.showMessageDialog(this, "Error deleting record: " + ex.getMessage());
}
    }//GEN-LAST:event_jButton9ActionPerformed

