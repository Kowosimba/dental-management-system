    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
      try {
    String cost, Name, qty, treatment, medicine;

    Name = txtname.getText();
    treatment = cmbtreatment.getSelectedItem() != null ? cmbtreatment.getSelectedItem().toString() : "";
    cost = txtcost.getText();
    medicine = txtmedicine.getText();
    qty = txtqty.getText();

    // Check for empty fields
    if (cost.isEmpty() || Name.isEmpty() || qty.isEmpty() || medicine.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields.");
        return; // Exit if any fields are empty
    }

    // Ask for confirmation before updating
    int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to update the prescription details for " + Name + "?", "Confirm Update", JOptionPane.YES_NO_OPTION);
    if (confirm != JOptionPane.YES_OPTION) {
        return; // Exit if user chooses not to proceed
    }

    Class.forName("com.mysql.jdbc.Driver");
    Connection con = DriverManager.getConnection("jdbc:mysql://localhost/dental", "root", "");
    
    // Prepare SQL statement
    PreparedStatement ps = con.prepareStatement("UPDATE prescriptions SET treatment = ?, cost = ?, medicine = ?, quantity = ? WHERE name = ?");

    // Set parameters for the update
    ps.setString(1, treatment); // Treatment
    ps.setString(2, cost); // Cost
    ps.setString(3, medicine); // Medicine
    ps.setString(4, qty); // Quantity
    ps.setString(5, Name); // Name as the identifier

    // Execute the update
    int rowsUpdated = ps.executeUpdate();
    
    // Check if any rows were updated
    if (rowsUpdated > 0) {
        JOptionPane.showMessageDialog(this, "Record updated successfully for " + Name);
        
       txtmedicine.setText(null);
        txtname.setText(null);
        txtcost.setText(null);
        txtqty.setText(null);
        cmbtreatment.setSelectedItem("Select Treatment");
        update_table();
    } else {
        JOptionPane.showMessageDialog(this, "No changes made. Please check if the details are different or if the name exists.");
    }

    // Close resources
    ps.close();
    con.close();

} catch (Exception ex) {
    Logger.getLogger(frmpatients.class.getName()).log(Level.SEVERE, null, ex);
    JOptionPane.showMessageDialog(this, "Error updating record: " + ex.getMessage());
}
    }//GEN-LAST:event_btnupdateActionPerformed

